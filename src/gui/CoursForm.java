    void load() {
    model.setRowCount(0); // Clear table
    for (Cours c : cs.findAll()) {
        model.addRow(new Object[]{
            c.getId(),
            c.getTitre(),
            c.getCategorie(),
            c.getDureeHeures()
        });
    }
   }
   void loadCategoriesFromDatabase() {
    comboCategorie.removeAllItems(); // Clear combo box
    comboCategorie.addItem(""); // Add empty first item
    
    // Use HashSet to avoid duplicates
    java.util.HashSet<String> uniqueCategories = new java.util.HashSet<>();
    
    // Get all courses
    List<Cours> coursList = cs.findAll();
    
    // Collect unique categories
    for (Cours cours : coursList) {
        String categorie = cours.getCategorie();
        if (categorie != null && !categorie.trim().isEmpty()) {
            uniqueCategories.add(categorie);
        }
    }
    
    // Add unique categories to combo box
    for (String categorie : uniqueCategories) {
        comboCategorie.addItem(categorie);
    }
}

