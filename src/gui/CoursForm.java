    private void btnAjouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAjouterActionPerformed
     String titre = txtTitre.getText();
    String categorie = comboCategorie.getSelectedItem().toString();
    String dureeText = txtDureeHeures.getText();
    
    if (titre.isEmpty() || categorie.isEmpty() || dureeText.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Veuillez remplir tous les champs!");
        return;
    }
    
    try {
        double duree = Double.parseDouble(dureeText);
        if (cs.create(new Cours(titre, categorie, duree))) {
            load();
            JOptionPane.showMessageDialog(this, "Cours ajouté avec succès!");
            // Clear fields
            txtTitre.setText("");
            comboCategorie.setSelectedIndex(0);
            txtDureeHeures.setText("");
        } else {
            JOptionPane.showMessageDialog(this, "Erreur lors de l'ajout!");
        }
      } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Durée doit être un nombre!");
      }
    }//GEN-LAST:event_btnAjouterActionPerformed

    private void btnModifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModifierActionPerformed
        if (id == 0) {
        JOptionPane.showMessageDialog(this, "Veuillez sélectionner un cours à modifier!");
        return;
    }
    
    String titre = txtTitre.getText();
    String categorie = comboCategorie.getSelectedItem().toString();
    String dureeText = txtDureeHeures.getText();
    
    if (titre.isEmpty() || categorie.isEmpty() || dureeText.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Veuillez remplir tous les champs!");
        return;
    }
    
    try {
        double duree = Double.parseDouble(dureeText);
        Cours cours = cs.findById(id);
        if (cours != null) {
            cours.setTitre(titre);
            cours.setCategorie(categorie);
            cours.setDureeHeures(duree);
            
            int reponse = JOptionPane.showConfirmDialog(this, 
                "Voulez-vous vraiment modifier ce cours?",
                "Confirmation",
                JOptionPane.YES_NO_OPTION);
            
            if (reponse == JOptionPane.YES_OPTION) {
                if (cs.update(cours)) {
                    load();
                    JOptionPane.showMessageDialog(this, "Cours modifié avec succès!");
                    // Clear fields
                    txtTitre.setText("");
                    comboCategorie.setSelectedIndex(0);
                    txtDureeHeures.setText("");
                    id = 0;
                } else {
                    JOptionPane.showMessageDialog(this, "Erreur lors de la modification!");
                }
            }
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Durée doit être un nombre!");
    }
    }//GEN-LAST:event_btnModifierActionPerformed

    private void btnSupprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSupprimerActionPerformed
         if (id == 0) {
        JOptionPane.showMessageDialog(this, "Veuillez sélectionner un cours à supprimer!");
        return;
    }
    
    int reponse = JOptionPane.showConfirmDialog(this, 
        "Voulez-vous vraiment supprimer ce cours?",
        "Confirmation",
        JOptionPane.YES_NO_OPTION);
    
    if (reponse == JOptionPane.YES_OPTION) {
        Cours cours = cs.findById(id);
        if (cours != null) {
            if (cs.delete(cours)) {
                load();
                JOptionPane.showMessageDialog(this, "Cours supprimé avec succès!");
                // Clear fields
                txtTitre.setText("");
                comboCategorie.setSelectedIndex(0);
                txtDureeHeures.setText("");
                id = 0;
            } else {
                JOptionPane.showMessageDialog(this, "Erreur lors de la suppression!");
            }
        }
    }
    }//GEN-LAST:event_btnSupprimerActionPerformed

    private void listeCoursMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listeCoursMouseClicked
        int selectedRow = listeCours.getSelectedRow();
        if (selectedRow >= 0) {
            id = Integer.parseInt(model.getValueAt(selectedRow, 0).toString());
            txtTitre.setText(model.getValueAt(selectedRow, 1).toString());
            comboCategorie.setSelectedItem(model.getValueAt(selectedRow, 2).toString());
            txtDureeHeures.setText(model.getValueAt(selectedRow, 3).toString());
        }
    }//GEN-LAST:event_listeCoursMouseClicked
          

